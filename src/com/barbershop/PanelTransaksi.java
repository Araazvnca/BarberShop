    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
            ComboItem item = (ComboItem) cbLayanan.getSelectedItem();
    if (item == null) {
        JOptionPane.showMessageDialog(this, "Pilih layanan dulu.");
        return;
    }
    idLayananList.add(item.id);
    hargaList.add(item.harga);
    DecimalFormat df = new DecimalFormat("#,###");

modelDetail.addRow(new Object[]{
    modelDetail.getRowCount() + 1,
    item.label,
    df.format(item.harga),
    "Hapus"
});
    hitungTotal();
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
            ComboItem pelanggan = (ComboItem) cbPelanggan.getSelectedItem();
    ComboItem pegawai = (ComboItem) cbPegawai.getSelectedItem();

    if (cbPelanggan.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "Pilih pelanggan dulu!");
        return;
    }
    if (cbPegawai.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "Pilih pegawai dulu!");
        return;
    }
    if (idLayananList.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Tambahkan minimal 1 layanan dulu!");
        return;
    }

    BigDecimal total = BigDecimal.ZERO;
    for (BigDecimal h : hargaList) total = total.add(h);

    try (Connection conn = Koneksi.getConnection()) {
        conn.setAutoCommit(false);
        int idTransaksi;
        try (PreparedStatement ps = conn.prepareStatement(
                "INSERT INTO transaksi (id_pelanggan, id_pegawai, total, status) VALUES (?,?,?,?)",
                Statement.RETURN_GENERATED_KEYS)) {
            ps.setInt(1, pelanggan.id);
            ps.setInt(2, pegawai.id);
            ps.setBigDecimal(3, total);
            ps.setString(4, "Menunggu");
            ps.executeUpdate();
            ResultSet keys = ps.getGeneratedKeys();
            keys.next();
            idTransaksi = keys.getInt(1);
        }
        try (PreparedStatement ps = conn.prepareStatement(
                "INSERT INTO transaksi_detail (id_transaksi, id_layanan, harga) VALUES (?,?,?)")) {
            for (int i = 0; i < idLayananList.size(); i++) {
                ps.setInt(1, idTransaksi);
                ps.setInt(2, idLayananList.get(i));
                ps.setBigDecimal(3, hargaList.get(i));
                ps.addBatch();
            }
            ps.executeBatch();
        }
        conn.commit();
        txtNoTransaksi.setText(String.valueOf(idTransaksi));
        JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan! No: " + idTransaksi);
        resetForm();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal simpan transaksi: " + e.getMessage());
    }
    }//GEN-LAST:event_btnBayarActionPerformed


