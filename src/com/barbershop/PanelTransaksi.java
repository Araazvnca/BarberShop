public void loadCombo() {
    cbPelanggan.removeAllItems();
    cbPegawai.removeAllItems();
    cbLayanan.removeAllItems();
    try (Connection conn = Koneksi.getConnection()) {
        if (conn == null) return;
        try (Statement st = conn.createStatement(); ResultSet rs = st.executeQuery("SELECT id_pelanggan, nama FROM pelanggan")) {
            while (rs.next()) ((javax.swing.JComboBox) cbPelanggan).addItem(new ComboItem(rs.getInt(1), rs.getString(2)));
        }
        try (Statement st = conn.createStatement(); ResultSet rs = st.executeQuery("SELECT id_pegawai, nama FROM pegawai WHERE role = 'Pegawai'")) {
    while (rs.next()) ((javax.swing.JComboBox) cbPegawai).addItem(new ComboItem(rs.getInt(1), rs.getString(2)));
}
        try (Statement st = conn.createStatement(); ResultSet rs = st.executeQuery("SELECT id_layanan, nama_layanan, harga FROM layanan")) {
            while (rs.next()) ((javax.swing.JComboBox) cbLayanan).addItem(
    new ComboItem(
        rs.getInt(1),
        rs.getString(2),
        rs.getBigDecimal(3)
    )
);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
    }
    
}

private void hitungTotal() {
    BigDecimal total = BigDecimal.ZERO;
    for (BigDecimal h : hargaList)
        total = total.add(h);

    DecimalFormat df = new DecimalFormat("#,###");
    lblTotal.setText("Total: Rp " + df.format(total));
}

private void resetForm() {
    idLayananList.clear();
    hargaList.clear();
    modelDetail.setRowCount(0);
    lblTotal.setText("Total: Rp 0");
    txtNoTransaksi.setText("");
    txtHarga.setText("");
    
    loadCombo();
}


